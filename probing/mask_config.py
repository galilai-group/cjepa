"""
Binary mask matrices for attention probing.

Each mask has shape (7, 16) where:
    - 7 = number of object slots
    - 16 = number of time steps
    
    1 = visible (context), 0 = masked (will be replaced with mask token)
    
The mask tokens receive attention from all other tokens in the bidirectional
transformer, so we can extract their attention patterns to understand what
information the model uses to predict masked slots.
"""

import torch

NUM_SLOTS = 7
NUM_TIMESTEPS = 16


def _make_mask(pattern: list[list[int]], name: str) -> torch.Tensor:
    """Convert nested list to a (7, 16) binary mask tensor and validate."""
    mask = torch.tensor(pattern, dtype=torch.bool)
    assert mask.shape == (NUM_SLOTS, NUM_TIMESTEPS), (
        f"Mask '{name}' has shape {mask.shape}, expected ({NUM_SLOTS}, {NUM_TIMESTEPS})"
    )
    return mask


# ============================================================================
# Mask Registry
# ============================================================================
MASK_REGISTRY: dict[str, torch.Tensor] = {}


def register_mask(name: str, pattern: list[list[int]]):
    """Register a named mask pattern."""
    MASK_REGISTRY[name] = _make_mask(pattern, name)


def get_mask(name: str) -> torch.Tensor:
    """Retrieve a mask by name. Raises KeyError if not found."""
    if name not in MASK_REGISTRY:
        raise KeyError(
            f"Mask '{name}' not found. Available masks: {list(MASK_REGISTRY.keys())}"
        )
    return MASK_REGISTRY[name].clone()


def list_masks() -> list[str]:
    """List all registered mask names."""
    return list(MASK_REGISTRY.keys())


